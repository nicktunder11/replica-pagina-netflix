
function toggleHide() {
    let btn = document.getElementById('btn1');
    let discription = document.getElementById('discription1');
    if (discription.style.display != 'none') {
        discription.style.display = 'none';
        btn1.innerHTML = '+';
    }
    else {
        discription.style.display = 'block';
        btn1.innerHTML = 'x';
    }
}

